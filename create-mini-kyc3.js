function emitRel32BranchBlock(cfg) {
  C(cfg.title);
  L(H(cfg.handlerId));
  for (let i = 0; i < rel32Handlers.length; i++) {
    const h = rel32Handlers[i];
    L(SET(0x41, h.id));
    L(CMP(0x50, 0x41));
    L(JE(cfg.caseBase + i) + ' ; ' + cfg.kind + ' target 0x' + h.id.toString(16).toUpperCase() + ' -> state_63');
  }
  L(JMP(0x45) + ' ; unknown ' + cfg.kind + ' target -> skip');
  for (let i = 0; i < rel32Handlers.length; i++) {
    const h = rel32Handlers[i];
    L(H(cfg.caseBase + i));
    L(GET(0x63, h.slot) + ' ; target offset for 0x' + h.id.toString(16).toUpperCase());
    L(JMP(cfg.joinLabel));
  }
  L(H(cfg.joinLabel));
  L(GET(0x64, 0x0E) + ' ; rel32 base = current offset');
  L(SET(0x41, cfg.instrSize));
  L(ADDV(0x64, 0x41) + ' ; rel32 base += ' + cfg.instrSize.toString(16).toUpperCase() + ' (' + cfg.kind + ' size)');
  L(SUBV(0x63, 0x64) + ' ; rel32 = target - base');
  L(SET(0x41, cfg.fragLen));
  L(CPY(0x02, cfg.fragOff, 0x00, cfg.fragLen) + ' ; emit fragment 0x' + cfg.fragOff.toString(16).toUpperCase());
  L(ADDV(0x0E, 0x41));
  L(GET(0x64, 0x02));
  L(SET(0x41, cfg.patchDisp));
  L(ADDV(0x64, 0x41));
  L('55 64 63 ; write rel32 at ' + cfg.kind + '+' + cfg.patchDisp.toString(16).toUpperCase());
  L(JMP(0x45));
  B();
}

