function makeStoreDwordFromStateFrag(ptrState, valueState) {
  const s = Buffer.alloc(16);
  s[0] = 0x49; s[1] = 0x8B; s[2] = 0x97;
  s.writeUInt32LE(ptrState * 8, 3);
  s[7] = 0x49; s[8] = 0x8B; s[9] = 0x87;
  s.writeUInt32LE(valueState * 8, 10);
  s[14] = 0x89; s[15] = 0x02;
  return s.toString('hex');
}
function makeLoadByteFixedFrag(dest, src) {
  const b = Buffer.alloc(17);
  b[0] = 0x49; b[1] = 0x8B; b[2] = 0x97;
  b.writeUInt32LE(src * 8, 3);
  b[7] = 0x0F; b[8] = 0xB6; b[9] = 0x02;
  b[10] = 0x49; b[11] = 0x89; b[12] = 0x87;
  b.writeUInt32LE(dest * 8, 13);
  return b.toString('hex');
}
function makeCopyBlobByStateFrag(destState, srcState, lenState) {
  const b = Buffer.alloc(36);
  b[0] = 0x49; b[1] = 0x8B; b[2] = 0xBF;
  b.writeUInt32LE(destState * 8, 3);
  b[7] = 0x48; b[8] = 0xBE;
  b.writeBigUInt64LE(0x140000000n + BigInt(RDATA_BASE), 9);
  b[17] = 0x49; b[18] = 0x8B; b[19] = 0x87;
  b.writeUInt32LE(srcState * 8, 20);
  b[24] = 0x48; b[25] = 0x01; b[26] = 0xC6;
  b[27] = 0x49; b[28] = 0x8B; b[29] = 0x8F;
  b.writeUInt32LE(lenState * 8, 30);
  b[34] = 0xF3; b[35] = 0xA4;
  return b.toString('hex');
}
