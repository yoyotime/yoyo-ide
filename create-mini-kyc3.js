function makeSetStateFrag(dest, imm) {
  const b = Buffer.alloc(17);
  b[0] = 0x48;
  b[1] = 0xB8;
  b.writeBigUInt64LE(BigInt(imm), 2);
  b[10] = 0x49;
  b[11] = 0x89;
  b[12] = 0x87;
  b.writeUInt32LE(dest * 8, 13);
  return b.toString('hex');
}
function makeCallFrag() {
  return Buffer.from([0xE8, 0x00, 0x00, 0x00, 0x00]).toString('hex');
}
